export const getProvincias = (): string[] => {
  return angolaLocations.map(loc => loc.provincia);
};

export const getMunicipiosByProvincia = (provincia: string): string[] => {
  const location = angolaLocations.find(loc => loc.provincia === provincia);
  return location ? location.municipios : [];
};

export const getAllMunicipios = (): string[] => {
  return angolaLocations.flatMap(loc => loc.municipios);
};
